<section class="section" id="sec-learn">
    <h2>What Is Offside?</h2>

    <div class="card">
        <h3>The Basic Rule</h3>
        <p>
            A player is in an <strong>offside position</strong> if, at the moment
            the ball is played by a team-mate:
        </p>
        <ul>
            <li>They are in the <strong>opponent's half</strong> of the pitch</li>
            <li>
                They are <strong>closer to the opponent's goal line</strong> than
                both the ball <em>and</em> the
                <strong>second-last opponent</strong> (the goalkeeper is usually the
                last)
            </li>
        </ul>
        <div class="highlight-box">
            <strong>Key phrase to remember:</strong> "Beyond the second-last
            opponent, in the opponents' half, when the ball is played."
        </div>
    </div>

    <div class="card">
        <h3>When Is It Actually an Offence?</h3>
        <p>
            Being in an offside <em>position</em> alone is <strong>not</strong> an
            offence. It only becomes one if, at the moment the ball is played by a
            team-mate, the player in an offside position:
        </p>
        <ul>
            <li>
                <strong>Interferes with play</strong> — touches or plays the ball
            </li>
            <li>
                <strong>Interferes with an opponent</strong> — obstructs their line
                of vision, challenges them, or clearly impacts their ability to play
                the ball
            </li>
            <li>
                <strong>Gains an advantage</strong> — plays a ball that rebounds
                or deflects off the goalpost, crossbar, match official, or an
                opponent; or plays a ball that has been deliberately saved by
                any opponent
            </li>
        </ul>
    </div>

    <div class="card">
        <h3>When Is It NOT Offside?</h3>
        <ul>
            <li>Receiving the ball directly from a <strong>goal kick</strong></li>
            <li>Receiving the ball directly from a <strong>throw-in</strong></li>
            <li>
                Receiving the ball directly from a <strong>corner kick</strong>
            </li>
            <li>The player is in their <strong>own half</strong> of the pitch</li>
            <li>
                The player is <strong>level with</strong> the second-last opponent
                (level is onside!)
            </li>
            <li>
                The player is <strong>behind the ball</strong> when it is played
            </li>
        </ul>
        <div class="warning-box">
            <strong>Common mistake:</strong> Many parents forget that offside does
            NOT apply at throw-ins, goal kicks, or corners. Keep this in mind!
        </div>
    </div>

    <div class="card">
        <h3>Body Parts That Count</h3>
        <p>
            Offside position is judged using the <strong>head, body, and
                feet</strong> only. <strong>Arms and hands do not count</strong>
            for any player, including goalkeepers. The boundary of the arm is
            defined as the bottom of the armpit — anything below that line is
            considered "arm" and is ignored.
        </p>
        <p>
            So if a player's arm is past the defender but their head, body, and
            feet aren't, they are <strong>onside</strong>.
        </p>
    </div>

