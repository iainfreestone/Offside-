            A player is in an <strong>offside position</strong> if, at the moment the ball is played by a team-mate,
            they are:
        </p>
        <ul>
            <li>In the <strong>opponents' half</strong> of the pitch</li>
            <li>Nearer to the opponents' goal line than both the <strong>ball</strong> and the <strong>second-last
                    opponent</strong></li>
        </ul>
        <p>
            Being in an offside position isn't itself an offence. It only becomes one when the player <strong>actively
                gets involved</strong> â€” by playing the ball, interfering with an opponent, or gaining an advantage.
        </p>
        <p>
            There are three important exceptions: a player <strong>cannot be offside</strong> when receiving the ball
            directly from
            a <strong>throw-in</strong>, <strong>goal kick</strong>, or <strong>corner kick</strong>.
        </p>
        <p>
            And the golden rule that helps attackers: if a player is exactly <strong>level with the second-last
