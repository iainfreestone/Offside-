    <h3>What Are Your Responsibilities?</h3>
    <p>
        Unlike qualified assistant referees at professional level, club assistant referees have a more limited role.
        The <strong>FA guidelines</strong> for grassroots state:
    </p>
    <ul>
        <li><strong>Indicate when the ball goes out of play</strong> ‚Äî point your flag in the direction of the team
            that gets the throw-in, goal kick, or corner</li>
        <li><strong>Flag for offside</strong> ‚Äî raise your flag straight up if you spot an offside offence</li>
        <li><strong>Assist the referee</strong> ‚Äî they may ask you to watch for specific things</li>
    </ul>
    <p>
        You do <strong>not</strong> need to worry about flagging for fouls (unless the referee asks), signalling
        substitutions, or tracking added time. The referee handles all of that.
    </p>

    <h3>Equipment Checklist</h3>
    <ul>
        <li><strong>A flag</strong> ‚Äî the club or referee will usually provide one</li>
        <li><strong>Comfortable shoes</strong> you can move quickly in ‚Äî you'll be jogging up and down the line</li>
        <li><strong>Weather-appropriate clothing</strong> ‚Äî standing still on a touchline in January is cold!</li>
        <li><strong>A positive attitude</strong> ‚Äî you're helping the kids play a fair game</li>
    </ul>
    <p>
        Some clubs recommend wearing a high-vis vest so the referee can spot you easily. Ask your team manager
        if they have one.
    </p>

    <h3>Which Side of the Pitch?</h3>
    <p>
        You'll usually be on the <strong>opposite side</strong> to any spectators and dugouts. The referee will
        tell you which side and which half to cover. You typically cover from the halfway line to the goal line
        nearest to you, swapping ends at half-time.
    </p>
    <p>
        Being on the opposite side means the referee has full view of your flag signals without spectators
        blocking the view. It also keeps you away from vocal parents on the sideline ‚Äî a bonus!
    </p>

    <h3>Positioning During Play</h3>
    <p>
        Your position is critical. You need to be <strong>in line with the second-last defender</strong> at all
        times. This means you're constantly moving ‚Äî jogging up and down the touchline to stay level.
    </p>
    <p>
        When play is in the other half of the pitch, stay near the halfway line. When it comes your way,
        start tracking the defensive line. The better your positioning, the better your decisions will be.
    </p>
    <p>
        Read more positioning tips in our <a href="../#tips" class="guide-link">Matchday Tips section</a>.
    </p>

    <h3>Communication With the Referee</h3>
    <p>
        Before kick-off, have a quick chat with the referee. Ask them:
    </p>
    <ul>
        <li>Which half am I covering?</li>
        <li>Do you just want me to flag offsides and throw-ins?</li>
        <li>Any particular things you'd like me to watch for?</li>
    </ul>
    <p>
        This shows the referee you're taking it seriously, and it helps them know what to expect from you.
        Most referees will appreciate the effort ‚Äî they're often volunteers too.
    </p>

    <h3>Flag Technique</h3>
    <p>
        Hold the flag in the hand <strong>nearest the referee</strong>, so it's most visible to them.
        You'll switch hands at half-time when you swap ends.
    </p>
    <ul>
        <li><strong>Throw-in:</strong> Point the flag in the direction of the team that gets the throw</li>
        <li><strong>Offside:</strong> Raise the flag straight up and hold it until the referee acknowledges</li>
        <li><strong>Goal kick / corner:</strong> Point towards the goal area or corner flag respectively</li>
    </ul>
    <p>
        Don't wave the flag frantically ‚Äî a calm, steady signal is easier for the referee to read.
    </p>

    <h3>What If I Get a Decision Wrong?</h3>
    <p>
        It happens. To everyone. At every level. The referee can overrule you ‚Äî that's their right and it's
        nothing personal. If it happens, just carry on. Don't dwell on it, don't argue, and definitely don't
