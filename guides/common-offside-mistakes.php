    <h3>2. Forgetting That "Level Is Onside"</h3>
    <p>
        If the attacker is exactly in line with the second-last opponent, they are <strong>onside</strong>.
        Many new linesmen flag this as offside because it "looks close." Remember: level always favours the
        attacker. When in doubt on a tight call, the flag stays down.
    </p>
    <p>
        At grassroots level, the players are moving quickly and you don't have VAR. If you can't be certain,
        the player is onside. Simple.
    </p>

    <h3>3. Watching the Ball Instead of the Players</h3>
    <p>
        This is the hardest habit to break. When a pass is made, your natural instinct is to follow the ball.
        But offside is judged at the <strong>moment the ball is played</strong> ‚Äî you need to be watching the
        defensive line and the attacker's position at that instant.
    </p>
    <p>
        Try using a <strong>"mental snapshot" technique</strong>: freeze the picture in your mind at the moment
        of the kick. Where was the second-last defender? Where was the attacker? That snapshot is your decision.
    </p>

    <h3>4. Not Knowing About the Goalkeeper</h3>
    <p>
        The offside rule talks about the "second-last opponent" ‚Äî not the "last defender." The goalkeeper usually
        counts as the last opponent, making the last outfield defender the second-last. But if the
        <strong>goalkeeper comes forward</strong> (e.g., for a corner), the GK might become the second-last
        opponent, and attackers who thought they were behind the last defender could actually be offside.
    </p>
    <p>
