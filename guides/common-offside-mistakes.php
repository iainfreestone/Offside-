    <h3>5. Flagging Immediately Instead of Waiting</h3>
    <p>
        At professional level, assistant referees use a technique called <strong>"wait and see"</strong>. If a
        player is in an offside position but doesn't touch the ball or interfere with play, there's no offence.
        Being in an offside <em>position</em> alone is not illegal ‚Äî the player has to become <strong>actively
            involved</strong>.
    </p>
    <p>
        Wait until they touch the ball or clearly impact an opponent before raising your flag. This avoids
        the embarrassing situation of flagging someone offside who then jogs away from the play entirely.
    </p>

    <h3>6. Ignoring the "Behind the Ball" Rule</h3>
    <p>
        A player is only offside if they're nearer to the goal line than <strong>both the ball and the
            second-last opponent</strong>. If the attacker is behind the ball when it's played, they cannot be
        offside ‚Äî even if they're past every defender on the pitch.
    </p>
    <p>
        This often happens with through balls played from deep positions, or when an attacker receives a
        back-pass. If they were behind the ball at the moment it was kicked, no offside.
    </p>

    <h3>7. Standing in the Wrong Position</h3>
    <p>
        If you're not in line with the second-last defender, you simply cannot judge offside accurately.
        You need to be looking straight across the pitch along the offside line. If you're behind or ahead
        of it, the angle makes everything look different.
    </p>
    <p>
        <strong>Keep moving</strong> ‚Äî stay level with the second-last defender at all times. This is your
        single most important job as a linesman. Position determines accuracy.
