    <h3>Can you be offside from a throw-in?</h3>
    <p>
        <strong>No.</strong> You cannot be offside when receiving the ball directly from a throw-in. This is one of
        three exceptions ‚Äî the others are goal kicks and corner kicks. This trips up more new linesmen than anything
        else. See mistake #1 in our <a href="common-offside-mistakes" class="guide-link">common mistakes guide</a>.
    </p>

    <h3>Can you be offside in your own half?</h3>
    <p>
        <strong>No.</strong> A player in their own half of the pitch cannot be in an offside position, regardless of
        where the defenders are. Even if they're the only player in their own half, they're fine.
    </p>

    <h3>What if the attacker is level with the defender?</h3>
    <p>
        <strong>Onside.</strong> "Level is onside" ‚Äî if the attacker is exactly in line with the second-last opponent
        when the ball is played, they are onside. The benefit always goes to the attacker.
    </p>

    <h3>Does the goalkeeper count as a defender for offside?</h3>
    <p>
        <strong>Yes.</strong> The rule refers to the "second-last opponent" ‚Äî the goalkeeper is usually the last
        opponent (nearest their own goal), making the last outfield defender the second-last. If the GK is out of
        position, the calculation changes. See our <a href="tricky-offside-scenarios" class="guide-link">tricky
            scenarios guide</a> for the "goalkeeper walkabout" situation.
    </p>

    <h3>What body parts count for offside?</h3>
    <p>
        <strong>Head, body, and feet only.</strong> Arms and hands are excluded ‚Äî for all players, including
        goalkeepers. The arm boundary is the bottom of the armpit. This is why VAR draws lines from the shoulder,
        not the hand.
    </p>

    <h3>What if a defender accidentally knocks the ball to an offside attacker?</h3>
    <p>
        It depends. If the ball <strong>deflects</strong> off a defender (uncontrolled), the offside still counts. If
        the defender <strong>deliberately plays</strong> the ball (a clearance, pass, or attempt to gain control),
        offside resets. A deliberate save, however, does <strong>not</strong> reset offside. This is covered in
        detail in our <a href="tricky-offside-scenarios" class="guide-link">tricky scenarios guide</a>.
    </p>

    <h3>When should I raise my flag?</h3>
    <p>
        Only when a player in an offside position <strong>becomes actively involved</strong> ‚Äî touches the ball,
        interferes with an opponent, or gains an advantage. Being in an offside position alone is not an offence.
        Use the "wait and see" approach.
    </p>

    <h3>Which hand do I hold the flag in?</h3>
    <p>
        The hand <strong>nearest the referee</strong>, so it's most visible to them. You'll switch hands at half-time
        when you swap ends. For more on flag technique, see our
        <a href="club-assistant-referee-guide" class="guide-link">complete AR guide</a>.
    </p>

    <h3>What if I'm not sure?</h3>
    <p>
        <strong>Keep your flag down.</strong> "Benefit of the doubt to the attacker" is the golden rule. It's far
        better to miss a marginal offside than to wrongly flag a legitimate goal-scoring chance.
    </p>

    <h3>I made a mistake and a parent shouted at me. What do I do?</h3>
    <p>
        <strong>Nothing.</strong> Smile, carry on, and focus on the next decision. It happens to every linesman at
        every level. You're volunteering your time to help the kids play ‚Äî that matters more than any single call.
    </p>

